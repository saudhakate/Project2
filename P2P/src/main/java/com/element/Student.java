package com.element;

public class Student {

	
	private int stdroll;
	private String stdname;
	private int stdclsid;
	private int stdsubid;
	public int getStdroll() {
		return stdroll;
	}
	public void setStdroll(int stdroll) {
		this.stdroll = stdroll;
	}
	public String getStdname() {
		return stdname;
	}
	public void setStdname(String stdname) {
		this.stdname = stdname;
	}
	public int getStdclsid() {
		return stdclsid;
	}
	public void setStdclsid(int stdclsid) {
		this.stdclsid = stdclsid;
	}
	public int getStdsubid() {
		return stdsubid;
	}
	public void setStdsubid(int stdsubid) {
		this.stdsubid = stdsubid;
	}
}
